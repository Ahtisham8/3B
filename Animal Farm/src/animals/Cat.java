package animals;

public class Cat 
{
	private String name;
	private String breed;
	private String color;
	private int age;
	
	/**
	 * @param name
	 * @param breed
	 * @param color
	 * @param age
	 */
	public Cat(String name, String breed, String color, int age) {
		super();
		this.name = name;
		this.breed = breed;
		this.color = color;
		this.age = age;
	}
	
}
